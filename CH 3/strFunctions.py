name="satyam kumar singh"

print(len(name)) #this print the length of string

print(name.endswith("singh"))#this give the boolean value

print(name.startswith("satyam"))#this also give the boolean value

print(name.capitalize()) #this capitalize the first element of the string

print(name.lower()) #this print the string in the lower case

print(name.upper()) #this print the string in the upper case

print(name.title()) #this will capitalize the frist element of every word

print(name.count("a")) #this count the number of time a particulat element present

print(name.find("kumar")) #this will return the index of the word

print(name.replace("satyam","golu")) #this replace the word satyam to golu